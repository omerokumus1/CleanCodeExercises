package Ch3_Functions.Ch3_2_SideEffects

/**
 * Side effects:
 *      - Functions can do something hidden.
 *          1. It may make changes to the variables of the class.
 *          2. It may make changes to the arguments.
 *          3. It may make changes to the system globals.
 *          They're all hidden if they are not specified in the function name.
 *
 *      - Temporal Coupling: A function to work properly, another thing must be ready.
 *      - Order dependency: A function to work properly, another function must be executed
 */